    function submitForm(formId, buttonId, noteId, redirectUrl) {
        $(formId).submit(function(event) {
            event.preventDefault();
            $(buttonId).text("Please Wait");
            $(buttonId).attr('disabled', 'disabled');

            // Create an object to hold form data
            var formData = {
                name: $(formId + ' [name="name"]').val(),
                email: $(formId + ' [name="email"]').val(),
                phone: $(formId + ' [name="phone"]').val(),
                message: $(formId + ' [name="message"]').val(),
                subject: "Villagio - 3 BHK Homes" // Change this to your desired subject value
            };

            $.ajax({
                type: "POST",
                url: "https://us-central1-email-js-1a09b.cloudfunctions.net/emailjs/submit-form",
                data: formData,
                success: function(msg) {
                    $(buttonId).removeAttr('disabled');
                    $(buttonId).text("Submit");
                    if (msg === 'Email sent successfully') {
                        result = '<p style="color:green; font-weight: 600;font-size: 18px;width:100%">Email Sent Successfully!</p>';
                        $(noteId).delay(20000).fadeOut();
                        $(formId)[0].reset();
                        // if (redirectUrl) {
                        //     window.location.href = redirectUrl;
                        // }
                    } else {
                        result = msg;
                    }
                    $(noteId).html(result);
                },
                error: function() {
                    $(buttonId).removeAttr('disabled');
                    $(buttonId).text("Submit");
                    $(noteId).html('<p style="color:red; font-weight: 600;font-size: 18px;width:100%">Error sending email!</p>');
                }
            });
            return false;
        });
    }

    $(document).ready(function() {
        submitForm("#header-form", "#header-btn", "#header-note");
        submitForm("#enquiry_form_footer", "#footer-button", "#note2");
        submitForm("#popup-form", "#popup-button", "#note3");
    });
